#!/bin/bash
# One-time setup: Deploy contract to Sepolia and manage custom image allowlist
set -eo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESEARCH_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
CONFIG_FILE="$RESEARCH_DIR/config.env"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

log() { echo -e "${GREEN}[$(date '+%H:%M:%S')]${NC} $1"; }
warn() { echo -e "${YELLOW}[$(date '+%H:%M:%S')] WARNING:${NC} $1"; }
error() { echo -e "${RED}[$(date '+%H:%M:%S')] ERROR:${NC} $1"; exit 1; }

usage() {
    cat <<EOF
Usage: $0 <command> [options]

Commands:
  deploy              Deploy BaseImageAllowlist contract to Sepolia
  add-image           Add RTMR1 (custom image) with support level
  remove-image        Remove a custom image from the allowlist
  set-min-support     Set minimum support level requirement
  set-min-svn         Set minimum SVN requirement
  show                Show current config and contract state

Note: MRTD (firmware) validation is now handled automatically by verifying
Google's signed endorsements from gs://gce_tcb_integrity/ovmf_x64_csm/tdx/
No manual MRTD management is required.

Options for 'deploy':
  --rpc-url URL       Sepolia RPC URL (default: from config or env)
  --private-key KEY   Private key for deployment (default: from config or env)

Options for 'add-image':
  --rtmr1 HASH        RTMR[1] measurement (48 bytes hex)
  --level LEVEL       Support level: EXPERIMENTAL, USABLE, STABLE, LATEST (default: LATEST)

Options for 'remove-image':
  --rtmr1 HASH        RTMR[1] measurement (48 bytes hex)

Options for 'set-min-support':
  --level LEVEL       Minimum support level: EXPERIMENTAL, USABLE, STABLE, LATEST

Options for 'set-min-svn':
  --svn NUMBER        Minimum SVN (Security Version Number)

Examples:
  # Deploy contract
  $0 deploy --rpc-url https://sepolia.infura.io/v3/YOUR_KEY --private-key 0x...

  # Add a custom image as LATEST
  $0 add-image --rtmr1 0x7bf10daf... --level LATEST

  # Set minimum support level to STABLE
  $0 set-min-support --level STABLE

  # Set minimum SVN to reject old firmware
  $0 set-min-svn --svn 3

  # Show current state
  $0 show
EOF
    exit 1
}

load_config() {
    if [ -f "$CONFIG_FILE" ]; then
        source "$CONFIG_FILE"
    fi
    # Environment variables override config file
    SEPOLIA_RPC_URL="${SEPOLIA_RPC_URL:-$RPC_URL}"
    PRIVATE_KEY="${PRIVATE_KEY:-}"
    CONTRACT_ADDR="${CONTRACT_ADDR:-}"
}

save_config() {
    cat > "$CONFIG_FILE" <<EOF
# Research Demo Configuration
# Generated by setup.sh

# Sepolia RPC URL
SEPOLIA_RPC_URL="$SEPOLIA_RPC_URL"

# Contract address (deployed by setup.sh deploy)
CONTRACT_ADDR="$CONTRACT_ADDR"

# Private key - uncomment and set, or use environment variable
# PRIVATE_KEY="0x..."
EOF
    log "Config saved to $CONFIG_FILE"
}

# Convert support level name to uint8
support_level_to_uint8() {
    case "$1" in
        NONE) echo 0 ;;
        EXPERIMENTAL) echo 1 ;;
        USABLE) echo 2 ;;
        STABLE) echo 3 ;;
        LATEST) echo 4 ;;
        *) error "Invalid support level: $1. Must be NONE, EXPERIMENTAL, USABLE, STABLE, or LATEST" ;;
    esac
}

deploy_contract() {
    log "Deploying BaseImageAllowlist to Sepolia..."

    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set. Use --rpc-url or set in config."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set. Use --private-key or set in environment."

    cd "$RESEARCH_DIR/contracts"

    # Build contract
    log "Building contract..."
    forge build

    # Deploy
    log "Deploying to Sepolia..."
    DEPLOY_OUTPUT=$(forge script script/Deploy.s.sol \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --broadcast \
        --private-key "$PRIVATE_KEY" \
        2>&1)

    echo "$DEPLOY_OUTPUT"

    # Extract contract address from output
    # Look for "BaseImageAllowlist deployed at:" or parse from broadcast log
    CONTRACT_ADDR=$(echo "$DEPLOY_OUTPUT" | grep -oP 'BaseImageAllowlist deployed at: \K0x[a-fA-F0-9]+' || true)

    if [ -z "$CONTRACT_ADDR" ]; then
        # Try to get from broadcast directory
        BROADCAST_FILE=$(ls -t "$RESEARCH_DIR/contracts/broadcast/Deploy.s.sol/"*/run-latest.json 2>/dev/null | head -1)
        if [ -f "$BROADCAST_FILE" ]; then
            CONTRACT_ADDR=$(jq -r '.transactions[0].contractAddress // empty' "$BROADCAST_FILE")
        fi
    fi

    if [ -z "$CONTRACT_ADDR" ]; then
        error "Could not extract contract address from deployment output"
    fi

    log "Contract deployed at: $CONTRACT_ADDR"
    save_config

    # Verify on Etherscan if API key is provided
    if [ -n "$ETHERSCAN_API_KEY" ]; then
        log "Verifying contract on Etherscan..."
        forge verify-contract "$CONTRACT_ADDR" \
            src/BaseImageAllowlist.sol:BaseImageAllowlist \
            --chain sepolia \
            --etherscan-api-key "$ETHERSCAN_API_KEY" \
            --watch \
            2>&1 || warn "Contract verification failed (contract may already be verified)"
        log "Contract verification complete"
        log "View on Etherscan: https://sepolia.etherscan.io/address/$CONTRACT_ADDR"
    else
        log "Skipping Etherscan verification (ETHERSCAN_API_KEY not set)"
        log "To verify later, run:"
        log "  ETHERSCAN_API_KEY=... forge verify-contract $CONTRACT_ADDR src/BaseImageAllowlist.sol:BaseImageAllowlist --chain sepolia --etherscan-api-key \$ETHERSCAN_API_KEY"
    fi

    echo ""
    log "Next steps:"
    log "1. Add your custom image: $0 add-image --rtmr1 0x... --level LATEST"
    log "2. (Optional) Set minimum support level: $0 set-min-support --level STABLE"
    log "3. (Optional) Set minimum SVN: $0 set-min-svn --svn 3"
    log ""
    log "Note: MRTD (firmware) validation is automatic - the KMS verifies"
    log "Google's signed endorsements from the TCB bucket at runtime."
}

add_image() {
    local rtmr1=""
    local level="LATEST"

    while [[ $# -gt 0 ]]; do
        case $1 in
            --rtmr1) rtmr1="$2"; shift 2 ;;
            --level) level="$2"; shift 2 ;;
            *) error "Unknown option: $1" ;;
        esac
    done

    [ -n "$rtmr1" ] || error "Missing --rtmr1"
    [ -n "$CONTRACT_ADDR" ] || error "CONTRACT_ADDR not set. Run 'deploy' first."
    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set."

    # Convert level to uint8
    level_uint8=$(support_level_to_uint8 "$level")

    # Normalize hex (ensure 0x prefix)
    rtmr1_hex="0x$(echo "$rtmr1" | sed 's/^0x//')"

    log "Adding custom image to allowlist..."
    log "  RTMR1: $rtmr1_hex"
    log "  Support Level: $level ($level_uint8)"
    log "  Contract: $CONTRACT_ADDR"

    cast send "$CONTRACT_ADDR" \
        "setImageSupport(bytes,uint8)" \
        "$rtmr1_hex" "$level_uint8" \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --private-key "$PRIVATE_KEY"

    log "Custom image added to allowlist!"

    # Verify
    log "Verifying..."
    SUPPORT=$(cast call "$CONTRACT_ADDR" \
        "getImageSupport(bytes)(uint8)" \
        "$rtmr1_hex" \
        --rpc-url "$SEPOLIA_RPC_URL")

    if [ "$SUPPORT" = "$level_uint8" ]; then
        log "Verification passed - image support level is $level"
    else
        error "Verification failed - unexpected support level: $SUPPORT"
    fi
}

remove_image() {
    local rtmr1=""

    while [[ $# -gt 0 ]]; do
        case $1 in
            --rtmr1) rtmr1="$2"; shift 2 ;;
            *) error "Unknown option: $1" ;;
        esac
    done

    [ -n "$rtmr1" ] || error "Missing --rtmr1"
    [ -n "$CONTRACT_ADDR" ] || error "CONTRACT_ADDR not set. Run 'deploy' first."
    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set."

    # Normalize hex (ensure 0x prefix)
    rtmr1_hex="0x$(echo "$rtmr1" | sed 's/^0x//')"

    log "Removing custom image from allowlist..."
    log "  RTMR1: $rtmr1_hex"
    log "  Contract: $CONTRACT_ADDR"

    cast send "$CONTRACT_ADDR" \
        "removeImage(bytes)" \
        "$rtmr1_hex" \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --private-key "$PRIVATE_KEY"

    log "Custom image removed from allowlist!"
}

set_min_support() {
    local level=""

    while [[ $# -gt 0 ]]; do
        case $1 in
            --level) level="$2"; shift 2 ;;
            *) error "Unknown option: $1" ;;
        esac
    done

    [ -n "$level" ] || error "Missing --level"
    [ -n "$CONTRACT_ADDR" ] || error "CONTRACT_ADDR not set. Run 'deploy' first."
    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set."

    # Convert level to uint8
    level_uint8=$(support_level_to_uint8 "$level")

    log "Setting minimum support level..."
    log "  Level: $level ($level_uint8)"
    log "  Contract: $CONTRACT_ADDR"

    cast send "$CONTRACT_ADDR" \
        "setMinimumSupportLevel(uint8)" \
        "$level_uint8" \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --private-key "$PRIVATE_KEY"

    log "Minimum support level set to $level!"
}

set_min_svn() {
    local svn=""

    while [[ $# -gt 0 ]]; do
        case $1 in
            --svn) svn="$2"; shift 2 ;;
            *) error "Unknown option: $1" ;;
        esac
    done

    [ -n "$svn" ] || error "Missing --svn"
    [ -n "$CONTRACT_ADDR" ] || error "CONTRACT_ADDR not set. Run 'deploy' first."
    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set."

    log "Setting minimum SVN..."
    log "  SVN: $svn"
    log "  Contract: $CONTRACT_ADDR"

    cast send "$CONTRACT_ADDR" \
        "setMinimumSVN(uint32)" \
        "$svn" \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --private-key "$PRIVATE_KEY"

    log "Minimum SVN set to $svn!"
}

show_config() {
    log "Current configuration:"
    echo ""
    echo "  Config file: $CONFIG_FILE"
    echo "  SEPOLIA_RPC_URL: ${SEPOLIA_RPC_URL:-<not set>}"
    echo "  CONTRACT_ADDR: ${CONTRACT_ADDR:-<not set>}"
    echo "  PRIVATE_KEY: ${PRIVATE_KEY:+<set>}${PRIVATE_KEY:-<not set>}"
    echo ""

    if [ -n "$CONTRACT_ADDR" ] && [ -n "$SEPOLIA_RPC_URL" ]; then
        log "Contract state:"
        echo ""

        OWNER=$(cast call "$CONTRACT_ADDR" "owner()(address)" --rpc-url "$SEPOLIA_RPC_URL" 2>/dev/null || echo "<unable to query>")
        echo "  Owner: $OWNER"

        MIN_SVN=$(cast call "$CONTRACT_ADDR" "minimumSVN()(uint32)" --rpc-url "$SEPOLIA_RPC_URL" 2>/dev/null || echo "<unable to query>")
        echo "  Minimum SVN: $MIN_SVN"

        MIN_SUPPORT=$(cast call "$CONTRACT_ADDR" "minimumSupportLevel()(uint8)" --rpc-url "$SEPOLIA_RPC_URL" 2>/dev/null || echo "<unable to query>")
        SUPPORT_NAMES=("NONE" "EXPERIMENTAL" "USABLE" "STABLE" "LATEST")
        if [[ "$MIN_SUPPORT" =~ ^[0-4]$ ]]; then
            echo "  Minimum Support Level: ${SUPPORT_NAMES[$MIN_SUPPORT]} ($MIN_SUPPORT)"
        else
            echo "  Minimum Support Level: $MIN_SUPPORT"
        fi

        echo ""
        log "MRTD (firmware) validation:"
        echo "  Verified automatically against Google's signed endorsements"
        echo "  Bucket: gs://gce_tcb_integrity/ovmf_x64_csm/tdx/"
        echo ""
        log "To check specific measurements:"
        echo "  cast call $CONTRACT_ADDR 'getImageSupport(bytes)(uint8)' 0x<rtmr1> --rpc-url \$SEPOLIA_RPC_URL"
    fi
}

# Main
load_config

case "${1:-}" in
    deploy)
        shift
        while [[ $# -gt 0 ]]; do
            case $1 in
                --rpc-url) SEPOLIA_RPC_URL="$2"; shift 2 ;;
                --private-key) PRIVATE_KEY="$2"; shift 2 ;;
                *) error "Unknown option: $1" ;;
            esac
        done
        deploy_contract
        ;;
    add-image)
        shift
        add_image "$@"
        ;;
    remove-image)
        shift
        remove_image "$@"
        ;;
    set-min-support)
        shift
        set_min_support "$@"
        ;;
    set-min-svn)
        shift
        set_min_svn "$@"
        ;;
    show)
        show_config
        ;;
    *)
        usage
        ;;
esac
