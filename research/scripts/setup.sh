#!/bin/bash
# One-time setup: Deploy contract to Sepolia and add base image measurements
set -eo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESEARCH_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
CONFIG_FILE="$RESEARCH_DIR/config.env"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

log() { echo -e "${GREEN}[$(date '+%H:%M:%S')]${NC} $1"; }
warn() { echo -e "${YELLOW}[$(date '+%H:%M:%S')] WARNING:${NC} $1"; }
error() { echo -e "${RED}[$(date '+%H:%M:%S')] ERROR:${NC} $1"; exit 1; }

usage() {
    cat <<EOF
Usage: $0 <command> [options]

Commands:
  deploy              Deploy BaseImageAllowlist contract to Sepolia
  add-image           Add base image measurements to allowlist
  show                Show current config

Options for 'deploy':
  --rpc-url URL       Sepolia RPC URL (default: from config or env)
  --private-key KEY   Private key for deployment (default: from config or env)

Options for 'add-image':
  --mrtd HASH         MRTD measurement (48 bytes hex)
  --rtmr0 HASH        RTMR[0] measurement (48 bytes hex)
  --rtmr1 HASH        RTMR[1] measurement (48 bytes hex)

Examples:
  $0 deploy --rpc-url https://sepolia.infura.io/v3/YOUR_KEY --private-key 0x...
  $0 add-image --mrtd 0x39e8dc49... --rtmr0 0x55248b07... --rtmr1 0x7bf10daf...
  $0 show
EOF
    exit 1
}

load_config() {
    if [ -f "$CONFIG_FILE" ]; then
        source "$CONFIG_FILE"
    fi
    # Environment variables override config file
    SEPOLIA_RPC_URL="${SEPOLIA_RPC_URL:-$RPC_URL}"
    PRIVATE_KEY="${PRIVATE_KEY:-}"
    CONTRACT_ADDR="${CONTRACT_ADDR:-}"
}

save_config() {
    cat > "$CONFIG_FILE" <<EOF
# Research Demo Configuration
# Generated by setup.sh

# Sepolia RPC URL
SEPOLIA_RPC_URL="$SEPOLIA_RPC_URL"

# Contract address (deployed by setup.sh deploy)
CONTRACT_ADDR="$CONTRACT_ADDR"

# Private key - uncomment and set, or use environment variable
# PRIVATE_KEY="0x..."
EOF
    log "Config saved to $CONFIG_FILE"
}

deploy_contract() {
    log "Deploying BaseImageAllowlist to Sepolia..."

    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set. Use --rpc-url or set in config."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set. Use --private-key or set in environment."

    cd "$RESEARCH_DIR/contracts"

    # Build contract
    log "Building contract..."
    forge build

    # Deploy
    log "Deploying to Sepolia..."
    DEPLOY_OUTPUT=$(forge script script/Deploy.s.sol \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --broadcast \
        --private-key "$PRIVATE_KEY" \
        2>&1)

    echo "$DEPLOY_OUTPUT"

    # Extract contract address from output
    # Look for "BaseImageAllowlist deployed at:" or parse from broadcast log
    CONTRACT_ADDR=$(echo "$DEPLOY_OUTPUT" | grep -oP 'BaseImageAllowlist deployed at: \K0x[a-fA-F0-9]+' || true)

    if [ -z "$CONTRACT_ADDR" ]; then
        # Try to get from broadcast directory
        BROADCAST_FILE=$(ls -t "$RESEARCH_DIR/contracts/broadcast/Deploy.s.sol/"*/run-latest.json 2>/dev/null | head -1)
        if [ -f "$BROADCAST_FILE" ]; then
            CONTRACT_ADDR=$(jq -r '.transactions[0].contractAddress // empty' "$BROADCAST_FILE")
        fi
    fi

    if [ -z "$CONTRACT_ADDR" ]; then
        error "Could not extract contract address from deployment output"
    fi

    log "Contract deployed at: $CONTRACT_ADDR"
    save_config

    # Verify on Etherscan if API key is provided
    if [ -n "$ETHERSCAN_API_KEY" ]; then
        log "Verifying contract on Etherscan..."
        forge verify-contract "$CONTRACT_ADDR" \
            src/BaseImageAllowlist.sol:BaseImageAllowlist \
            --chain sepolia \
            --etherscan-api-key "$ETHERSCAN_API_KEY" \
            --watch \
            2>&1 || warn "Contract verification failed (contract may already be verified)"
        log "Contract verification complete"
        log "View on Etherscan: https://sepolia.etherscan.io/address/$CONTRACT_ADDR"
    else
        log "Skipping Etherscan verification (ETHERSCAN_API_KEY not set)"
        log "To verify later, run:"
        log "  ETHERSCAN_API_KEY=... forge verify-contract $CONTRACT_ADDR src/BaseImageAllowlist.sol:BaseImageAllowlist --chain sepolia --etherscan-api-key \$ETHERSCAN_API_KEY"
    fi

    echo ""
    log "Next steps:"
    log "1. Get the hex quote from KMS logs, then extract measurements: ./research/scripts/decode-quote.sh <quote-hex>"
    log "2. Add measurements: $0 add-image --mrtd 0x... --rtmr0 0x... --rtmr1 0x..."
}

add_base_image() {
    local mrtd=""
    local rtmr0=""
    local rtmr1=""

    while [[ $# -gt 0 ]]; do
        case $1 in
            --mrtd) mrtd="$2"; shift 2 ;;
            --rtmr0) rtmr0="$2"; shift 2 ;;
            --rtmr1) rtmr1="$2"; shift 2 ;;
            *) error "Unknown option: $1" ;;
        esac
    done

    [ -n "$mrtd" ] || error "Missing --mrtd"
    [ -n "$rtmr0" ] || error "Missing --rtmr0"
    [ -n "$rtmr1" ] || error "Missing --rtmr1"
    [ -n "$CONTRACT_ADDR" ] || error "CONTRACT_ADDR not set. Run 'deploy' first."
    [ -n "$SEPOLIA_RPC_URL" ] || error "SEPOLIA_RPC_URL not set."
    [ -n "$PRIVATE_KEY" ] || error "PRIVATE_KEY not set."

    # Normalize hex (ensure 0x prefix)
    mrtd_hex=$(echo "$mrtd" | sed 's/^0x//')
    rtmr0_hex=$(echo "$rtmr0" | sed 's/^0x//')
    rtmr1_hex=$(echo "$rtmr1" | sed 's/^0x//')

    log "Adding base image to allowlist..."
    log "  MRTD:  0x$mrtd_hex"
    log "  RTMR0: 0x$rtmr0_hex"
    log "  RTMR1: 0x$rtmr1_hex"
    log "  Contract: $CONTRACT_ADDR"

    cast send "$CONTRACT_ADDR" \
        "addBaseImage(bytes,bytes,bytes)" \
        "0x$mrtd_hex" "0x$rtmr0_hex" "0x$rtmr1_hex" \
        --rpc-url "$SEPOLIA_RPC_URL" \
        --private-key "$PRIVATE_KEY"

    log "Base image added to allowlist!"

    # Verify
    log "Verifying..."
    ALLOWED=$(cast call "$CONTRACT_ADDR" \
        "isAllowed(bytes,bytes,bytes)(bool)" \
        "0x$mrtd_hex" "0x$rtmr0_hex" "0x$rtmr1_hex" \
        --rpc-url "$SEPOLIA_RPC_URL")

    if [ "$ALLOWED" = "true" ]; then
        log "Verification passed - base image is in allowlist"
    else
        error "Verification failed - base image not in allowlist"
    fi
}

show_config() {
    log "Current configuration:"
    echo ""
    echo "  Config file: $CONFIG_FILE"
    echo "  SEPOLIA_RPC_URL: ${SEPOLIA_RPC_URL:-<not set>}"
    echo "  CONTRACT_ADDR: ${CONTRACT_ADDR:-<not set>}"
    echo "  PRIVATE_KEY: ${PRIVATE_KEY:+<set>}${PRIVATE_KEY:-<not set>}"
    echo ""

    if [ -n "$CONTRACT_ADDR" ] && [ -n "$SEPOLIA_RPC_URL" ]; then
        log "Contract owner:"
        cast call "$CONTRACT_ADDR" "owner()(address)" --rpc-url "$SEPOLIA_RPC_URL" 2>/dev/null || echo "  <unable to query>"
    fi
}

# Main
load_config

case "${1:-}" in
    deploy)
        shift
        while [[ $# -gt 0 ]]; do
            case $1 in
                --rpc-url) SEPOLIA_RPC_URL="$2"; shift 2 ;;
                --private-key) PRIVATE_KEY="$2"; shift 2 ;;
                *) error "Unknown option: $1" ;;
            esac
        done
        deploy_contract
        ;;
    add-image)
        shift
        add_base_image "$@"
        ;;
    show)
        show_config
        ;;
    *)
        usage
        ;;
esac
